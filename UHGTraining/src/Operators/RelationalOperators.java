package Operators;

public class RelationalOperators {

	public static void main(String[] args) {
		int x= 10;
		int y = 20;
		 boolean results = x ==y;
		 System.out.println("x == y ? "+results);

		 results = x != y;
		 System.out.println("x != y ? "+results);
		 
		 results = x > y;
		 System.out.println("x > y ? "+results);
		 
		 results = x >= y;
		 System.out.println("x >= y ? "+results);
		 
		 results = x < y;
		 System.out.println("x < y ? "+results);
		 
		 results = x <= y;
		 System.out.println("x <= y ? "+results);
	}

}
