package Operators;

public class ArthameticOperators {

	public static void main(String[] args) {
		int a = 10;
		int b= 20;
		 int results = a+b;
		 
		 
		  System.out.println("a + b = "+results);
		  results= a - b;
		  
		  System.out.println("a - b = "+results);
		  results= a * b;
		  
		  System.out.println("a  * b = "+results);
		  results= a / b;
		 
		  System.out.println("a / b = "+results);
		  results= a % 2;
		  
		  System.out.println("a % 2 = "+results);
		  
		  
	}

}
